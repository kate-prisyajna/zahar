echo "Площадь = $s см<sup>2</sup><br>";

$aa=round(rad2deg(sin($a / $c)));           //Вычисляем угол по формуле sin и переводим радианы в градусы.
$bb=90 - $aa;
echo "Углы:
$aa &deg 
$bb &deg
90 &deg";

