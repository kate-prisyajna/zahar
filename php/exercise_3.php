<?php
$a=$_POST["one"];   //присваиваю переменным значения, которые получили в методе POST
$b=$_POST["two"];   //Это длина наших катетов


echo "Если у нашего прямоугольного треугольника: <br>Первый катет = " . $a . "см<br>";  //Выводим значения на экран
